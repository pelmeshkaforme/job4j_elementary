package ru.job4j.math;

public class MathAbsUsage {
    public static void main(String[] args) {
        int i = 10;
        int j = 3;
        System.out.println("Разность чисел равна: " + (j - i));
        System.out.println("Абсолютное значение разности чисел равно: " + Math.abs(j - i));
    }
}
